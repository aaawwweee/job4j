package ru.job4j.max;

/**
 * Возвращение максимального числа из 2
 * @version 1
 * @author Alexander Kashkin
 * @since 01.01.2019
 */
public class Max {
    /**
     *
     * @param first - first
     * @param second - second
     * @return - first or second
     */
    public int max(int first, int second){
        return (first > second) ? first : second;
    }

}
